import UIKit

extension UIFont {
    static var veryLargeHeader: UIFont {
        return UIFont(name: "Roboto-Bold", size: 30)!
    }
    
    static var largeHeader: UIFont {
        return UIFont(name: "Roboto-Bold", size: 24)!
    }
    
    static var header: UIFont {
        return UIFont(name: "Roboto-Regular", size: 24)!
    }
    
    static var smallHeader: UIFont {
        return UIFont(name: "Roboto-Medium", size: 20)!
    }
    
    static var bigInfoText: UIFont {
        return UIFont(name: "Roboto-Regular", size: 16)!
    }
    
    static var infoText: UIFont {
        return UIFont(name: "Roboto-Medium", size: 14)!
    }
    
    static var smallInfoText: UIFont {
        return UIFont(name: "Roboto-Regular", size: 14)!
    }
    
    static var numberFont: UIFont {
        return UIFont(name: "Roboto-Bold", size: 80)!
    }
}
